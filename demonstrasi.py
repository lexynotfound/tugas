import math

    ## operator aritmatika jumlah
def demonstrasi_perjumlahan():
    angka1=90
    angka2=20

    print("Perjumlahan       \t:", angka1+angka2)
    print("Akar dari         \t:", math.pow(3,3))


    ## operator aritmatika pengurangan
def demonstrasi_pengurangan():
    angka1=90
    angka2=20

    print("Pengurangan       \t:", angka1-angka2)
    print("Akar dari         \t:", math.pow(5,5))


    ## operator aritmatika pembagian
def demonstrasi_pembagian():
    angka1=100
    angka2=100

    print("Pembagian       \t:", angka1//angka2)
    print("Akar dari       \t:", math.pow(4,4))

    ## operator aritmatika peerkalian
def demonstrasi_perkalian():
    angka1=80
    angka2=100

    print("Perkalian       \t:",angka1*angka2)
    print("Akar dari       \t: ", math.pow(10,10))

demonstrasi_perjumlahan()
demonstrasi_pengurangan()
demonstrasi_pembagian()
demonstrasi_perkalian()
